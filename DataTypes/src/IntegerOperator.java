//displays output of the various arithmatic operator (input within program)

public class IntegerOperator {
    public static void main(String[] args) {
        int a=5,b=10;

        System.out.println("Addition of 2 nos. is: "+(a+b));
        System.out.println("Subtraction of 2 nos. is :"+(a-b));
        System.out.println("Multiplcation of 2 nos. is :"+a*b);
        System.out.println("Division of 2 nos. is :"+a/b);
        System.out.println("Modulus of 2 ns. is :"+a%b);
    }


}
